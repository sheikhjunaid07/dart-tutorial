import 'dart:math';

void main(){
  int num1 = 10;
  int num2 = 2;

  num power = pow(10, 2);
  num minimum = min(10, 2);
  num maximum = max(10, 2);
  num square = sqrt(25);

  print("Power of ${num1} is ${power}");
  print("Minimum number between ${num1} and ${num2} is ${minimum} ");
  print("Maximum number between ${num1} and ${num2} is ${maximum}");
  print("Square root of 25 is ${square}");

}
